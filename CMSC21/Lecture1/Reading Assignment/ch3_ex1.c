#include <stdio.h>

int main (){
    
    int i, j; //declaring the variable i and j as integer
    float x, y; //declaring the variable x and y as integer
    
    i=10; //assigns a value to variable i in integer form
    j=20; //assigns a value to variable j in integer form
    x=43.2892f; //assigns a value to variable x and y in float type 
    y=5527.0f; //with f at end of value to further establish its type
    
    //will print the output by finding the values assigned in each variables
    printf("i = %d, j = %d, x = %f, y = %f\n", i, j, x, y); 

}